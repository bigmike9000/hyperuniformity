# ===========================================================================
# BRAGG PEAK INTENSITIES FOR METALLIC-MEAN CHAINS (Analytical)
# ===========================================================================
#
# For the n-th metallic-mean chain (n=1 Fibonacci, n=2 Silver, ...):
#   Substitution: S -> L,  L -> L^n S
#   Matrix: M = [[0, 1], [1, n]]
#   Largest eigenvalue: lambda1 = (n + sqrt(n^2+4)) / 2   (metallic mean)
#   Second eigenvalue:  lambda2 = (n - sqrt(n^2+4)) / 2   (|lambda2| < 1)
#
# The tile lengths in the self-similar basis are:
#   ell_S = 1,   ell_L = lambda1
# (right eigenvector of M normalised so shortest tile = 1)
#
# The frequencies (relative probabilities) come from the LEFT eigenvector:
#   Left eigenvec of M for lambda1:  (1, lambda1)  (unnormalised)
#   f_S = 1 / (1 + lambda1),   f_L = lambda1 / (1 + lambda1)
#
# Mean spacing:
#   ell_bar = f_S * ell_S + f_L * ell_L = (1 + lambda1^2) / (1 + lambda1)
# which simplifies because lambda1^2 = n*lambda1 + 1 (characteristic equation):
#   ell_bar = (1 + n*lambda1 + 1) / (1 + lambda1) = (2 + n*lambda1)/(1+lambda1)
#
# Density: rho = 1 / ell_bar
#
# STRUCTURE FACTOR / BRAGG PEAKS:
# For a one-dimensional quasiperiodic tiling generated by cut-and-project from
# Z^2 with the cut window defined by the substitution, the Bragg peaks lie at
# wavevectors
#
#   G_{m,n} = 2*pi / ell_bar * (m + n / lambda1)
#
# for (m, n) integers, not both zero.  The intensities are given by:
#
#   I_{m,n} = |A_{m,n}|^2
#
# where (using the geometric structure factor of the two-tile unit cell):
#
#   A_{m,n} = f_S * ell_S * sinc(G * ell_S / 2) * exp(i G * ell_S/2)
#            + f_L * ell_L * sinc(G * ell_L / 2) * exp(i G * ell_L/2)
#                                                              * exp(i G * ell_S)
#
# BUT for a point process (vertices of tiles, not centres), the structure factor
# of the POINT PROCESS is simply the squared modulus of the sum of phase factors.
#
# The correct analytical formula for the structure factor of the point process
# of a two-letter substitution tiling is derived in the literature (see e.g.
# Queffelec 1987, Hof 1995, Oguz 2019).  For our purposes we use the most
# direct approach: the structure factor at wavevector k is
#
#   S(k) = (1/N) |sum_{j=1}^N exp(i k x_j)|^2
#
# where x_j are the tile ENDPOINTS (= particle positions).
# For large N this is dominated by the Bragg peaks.
#
# We compute I_G = lim_{N->inf} (1/N) |sum_j exp(i G x_j)|^2 using the
# transfer-matrix / eigenfunction method described in:
#   Queffelec (1987), Godrece & Luck (1990), Oguz et al. (2019).
#
# For the two-letter metallic-mean chain the ANALYTICAL intensity formula is:
#
#   I_{m,n}  =  |F(q_perp)|^2
#
# where q_perp = 2*pi * n / lambda1  (the "perpendicular" component in the
# 2D embedding space) and F(q) is the WINDOW FUNCTION of the acceptance strip:
#
#   F(q) = (1/|W|) * integral_{-W/2}^{W/2} exp(i q t) dt
#         = sinc(q * W / 2)   (with sinc(x) = sin(x)/x)
#
# where W = ell_S = 1 is the width of the acceptance strip (the "perpendicular"
# tile length for the short tile).  This is the standard cut-and-project result.
#
# HOWEVER, this formula applies to the DENSITY of tile centres or tile left
# endpoints only if we use the correct perpendicular tile length.  For the
# standard metallic-mean chain with tiles S=1 and L=lambda1, the perpendicular
# tile lengths are ell_S^perp = lambda1 and ell_L^perp = 1 (they are SWAPPED
# relative to the physical lengths -- this is the self-duality of the cut-and-
# project construction).
#
# The exact intensity is:
#
#   I_{m,n} = rho^2 * ell_bar^2 * |F_perp(q_perp)|^2
#
# where q_perp = 2*pi*(m*lambda2 + n) / (lambda1 - lambda2)  and
# the window function is a sum over the two tile types:
#
#   F_perp(q_perp) = (1/ell_bar) *
#       [f_S * ell_L^perp * sinc(q_perp * ell_L^perp / 2)  (contribution of S tiles)
#      + f_L * ell_S^perp * sinc(q_perp * ell_S^perp / 2)] (contribution of L tiles)
#
# where ell_S^perp = lambda1  and  ell_L^perp = -lambda2 = |lambda2| * sign...
#
# This is getting complicated.  We use a cleaner, equivalent formulation:
# the ANALYTICAL INTENSITY for the point process of a two-letter substitution
# tiling (Bragg component only) is:
#
#   I_{m,n} = |hat{rho}(G_{m,n})|^2 / rho
#
# where hat{rho}(G) = (1/L) sum_j exp(i G x_j).  In the infinite-volume limit
# (for Bragg peaks only):
#
#   hat{rho}(G) = rho * (f_S * exp(i * phi_S(G)) + f_L * exp(i * phi_L(G)))
#                * W_perp(q_perp(G))
#
# This is non-trivial to evaluate analytically.  Instead we use the following
# well-known result for the Fibonacci-type chains (see Bombieri & Taylor 1986,
# and the review in Senechal "Quasicrystals and Geometry", Chapter 5):
#
# For the n-th metallic mean chain, the structure factor has Bragg peaks at
# G_{p,q} = 2*pi*(p + q*lambda1) / ell_bar,   p,q in Z,
# with intensities:
#
#   I_{p,q} = sinc^2(pi * q / lambda1)  *  (GEOMETRIC PHASE FACTOR)
#
# The cleanest approach for our purpose is:
#
#  DIRECT NUMERICAL METHOD (Method B):
#   Generate the tiling to N ~ 10^6 tiles, compute S(k) via FFT on a
#   dense k-grid, identify peaks, sum I_G/G^2.
#
# This gives a semi-analytical result: analytically exact positions from the
# substitution structure, numerically accurate intensities from the FFT.
#
# ===========================================================================

def metallic_mean(n):
    """Return the n-th metallic mean: (n + sqrt(n^2+4)) / 2."""
    return (n + np.sqrt(n**2 + 4)) / 2.0


def chain_params(n):
    """
    Compute key parameters for the n-th metallic-mean chain.

    Returns dict with:
      lambda1  : metallic mean (largest eigenvalue)
      lambda2  : second eigenvalue (= n - lambda1, negative for n >= 1)
      ell_S    : short tile length = 1
      ell_L    : long  tile length = lambda1
      f_S      : frequency of S tiles
      f_L      : frequency of L tiles
      ell_bar  : mean tile length = 1/rho
      rho      : number density of points
      name     : chain name string
    """
    lam1 = metallic_mean(n)
    lam2 = n - lam1                  # (n - sqrt(n^2+4))/2 < 0
    ell_S = 1.0
    ell_L = lam1
    # Left eigenvector of M = [[0,1],[1,n]] for eigenvalue lam1:
    # M^T v = lam1 v  =>  v = (1, lam1) (unnormalized)
    # So f_S proportional to 1, f_L proportional to lam1:
    f_S = 1.0 / (1.0 + lam1)
    f_L = lam1 / (1.0 + lam1)
    ell_bar = f_S * ell_S + f_L * ell_L
    rho = 1.0 / ell_bar
    names = {1: 'Fibonacci', 2: 'Silver', 3: 'Bronze', 4: 'Copper', 5: 'Nickel'}
    return dict(lambda1=lam1, lambda2=lam2, ell_S=ell_S, ell_L=ell_L,
                f_S=f_S, f_L=f_L, ell_bar=ell_bar, rho=rho,
                name=names.get(n, f'Metallic-{n}'))


# ===========================================================================
# METHOD A: ANALYTICAL BRAGG PEAKS via cut-and-project formula
# ===========================================================================
#
# For a two-letter substitution tiling with metallic mean lambda1, the
# cut-and-project embedding places the tiling as a cut through Z^2.
# The Bragg peaks at G_{p,q} = 2*pi*(p + q*lambda1)/ell_bar have intensities:
#
#   I_{p,q} = |A(p,q)|^2
#
# where the GEOMETRIC FORM FACTOR A(p,q) accounts for the two tile types.
# Using the derivation of Bombieri & Taylor (1986) and the explicit formula
# in Oguz et al. (2019, Supplemental), for POINTS AT LEFT ENDPOINTS of tiles:
#
# The intensity of the Bragg peak (p,q) is given by:
#
#   |A(p,q)|^2 = [sinc(pi * (p * lambda2_norm + q))]^2
#
# where lambda2_norm = lambda2 / lambda1 ... actually let us use the more
# explicit derivation.
#
# The point process of a metallic-mean tiling is EQUIVALENT (by cut-and-project)
# to taking integer points in 1D projected via:
#   x_k = k * e_par + f(k) * e_perp
# where e_par, e_perp define the cut angle.
#
# A cleaner formula: for the FIBONACCI chain specifically, the structure factor
# intensity is given by (Cahn & Gratias 1986; see also Senechal):
#
#   I_{p,q} = sinc^2(pi * q * ell_S / ell_bar) * rho^2 * ell_bar^2
#
# where sinc(x) = sin(x)/x.  Wait, that is the formula for a CUT-AND-PROJECT
# with rectangular window of width ell_S in the perpendicular direction.
#
# For the general n-th metallic mean chain, the perpendicular window has
# width W_perp = ell_S = 1, and the perpendicular component of the
# reciprocal lattice vector (p,q) is:
#   q_perp = 2*pi * (-p/lambda1 + q)  ... this comes from the 2D reciprocal
#            lattice rotated to the physical + perpendicular axes.
#
# More precisely: the cut-and-project construction embeds the chain in R^2
# with the physical direction e_par = (1, lambda1)/sqrt(1+lambda1^2) and
# the perpendicular e_perp = (-lambda1, 1)/sqrt(1+lambda1^2).
# The 2D reciprocal lattice vectors are h_{p,q} = (p, q) in units of 2*pi.
# Their physical and perpendicular components are:
#   G = h . e_par * 2*pi / (norm of projected lattice)
#   G_perp = h . e_perp * 2*pi / (norm of projected lattice)
#
# In natural units where ell_bar = 1/rho:
#   G_{p,q}     = 2*pi*rho * (p + q*lambda1) / (1+lambda1^2) * sqrt(1+lambda1^2)
#               = 2*pi*rho * (p + q*lambda1) / sqrt(1+lambda1^2)
#
# But in practice we know G_{p,q} = 2*pi*(p + q*lambda1) / ell_bar directly
# from the quasiperiodic property of the tiling.
#
# The PERPENDICULAR wavevector component is:
#   G_perp_{p,q} = 2*pi * (-p*lambda1 + q) / sqrt(1+lambda1^2)   (up to a scale)
#
# Actually, the simplest exact formula for the Bragg intensities uses:
#
#   I_{p,q} = (rho * ell_bar)^2 * |W_hat(G_perp)|^2
#           = |W_hat(G_perp)|^2    (since rho * ell_bar = 1)
#
# where W_hat(q) is the Fourier transform of the WINDOW FUNCTION w(t) on [-W/2, W/2]:
#   W_hat(q) = sinc(q * W / 2) = sin(q*W/2) / (q*W/2)
#
# and W = ell_S = 1 is the window width.
#
# The perpendicular component G_perp_{p,q} (unnormalised) is:
#   G_perp_{p,q} = p * (-1/lambda1) + q * 1 = q - p/lambda1
# (this is the "star" map: (p,q) -> q - p/lambda1 = p*lambda2_neg + q
#  where lambda2_neg = -1/lambda1 since lambda1*|lambda2| = 1 for metallic means)
#
# Actually for metallic means: lambda1 * |lambda2| = |lambda1 * lambda2| = |det M| = 1.
# So |lambda2| = 1/lambda1.  And lambda2 = n - lambda1 < 0, so lambda2 = -1/lambda1.
#
# Thus: G_perp_{p,q} = q - p/lambda1 = q + p*lambda2  (since lambda2 = -1/lambda1)
#
# The window function: W_hat(G_perp) = sinc(pi * G_perp * ell_S)
#   = sinc(pi * (q + p*lambda2)) = sinc(pi*(q - p/lambda1))
#
# So the EXACT ANALYTICAL formula for Bragg peak intensities is:
#
#   I_{p,q} = sinc^2(pi * (q - p/lambda1))
#
# with sinc(x) = sin(x)/x (unnormalised sinc).
#
# VERIFICATION for p=0, q=0 (DC term):
#   I_{0,0} = sinc^2(0) = 1  -> this is the zero wavevector peak,
#   excluded from the sum since G_{0,0} = 0.
#
# The structure factor S(k) satisfies S(0) = 0 for hyperuniform systems.
# Since I_{0,0} would correspond to G=0, it must be excluded (or it
# represents the mean density contribution that is subtracted).
#
# For the VARIANCE formula, we use the CONNECTED structure factor:
#   S_c(k) = S(k) - rho * delta(k)  (removes the forward-scattering peak)
# which for a hyperuniform system satisfies S_c(k) -> 0 as k -> 0.
#
# The BRAGG PEAKS of S_c(k) are at all G != 0:
#   S_c(G_{p,q}) = N * I_{p,q} / rho  (per unit length normalization)
#
# In the standard normalization where S(k) = (1/N)|sum exp(ikx)|^2, the
# Bragg peaks have height proportional to N (delta functions).  In the
# continuum/thermodynamic limit, we write:
#   S(k) = sum_{G} I_G * delta(k - G) + continuous_part
#
# where I_G are the SPECIFIC INTENSITIES (per unit density).  For the
# number variance formula we need:
#   Lambda_bar = (2*rho/pi) * sum_{G > 0} I_G / G^2
#
# where I_G is normalized such that I_G = S(G)/rho in the finite-N formula.
# The correct normalization for the cut-and-project formula gives I_G
# directly from sinc^2 (no extra factors of rho), and we must check against
# known results.
#
# CALIBRATION: For the Fibonacci chain (n=1), the literature gives
# Lambda_bar = 0.20110.  We tune the normalization to reproduce this.


def bragg_peaks_analytical(n, p_max=200, q_max=200):
    """
    Compute Bragg peak positions G and intensities I for the n-th metallic-
    mean chain using the cut-and-project / window-function formula.

    Parameters
    ----------
    n : int
        Metallic-mean index (1=Fibonacci, 2=Silver, 3=Bronze, ...).
    p_max, q_max : int
        Range of integer indices to sum over: p in [-p_max, p_max],
        q in [-q_max, q_max].

    Returns
    -------
    G_pos : ndarray
        Positive Bragg wavevectors, sorted.
    I_pos : ndarray
        Corresponding intensities.
    params : dict
        Chain parameters.
    """
    p = chain_params(n)
    lam1 = float(p['lambda1'])
    ell_bar = float(p['ell_bar'])

    # Window function: sinc(pi*(q - p/lambda1))
    # with sinc(x) = sin(x)/x (returns 1 at x=0)
    def sinc_unnorm(x):
        # Numerically stable
        result = np.where(np.abs(x) < 1e-14, 1.0, np.sin(np.pi * x) / (np.pi * x))
        return result

    # Build list of (G, I) pairs
    pv = np.arange(-p_max, p_max + 1)
    qv = np.arange(-q_max, q_max + 1)
    PP, QQ = np.meshgrid(pv, qv, indexing='ij')
    PP = PP.ravel()
    QQ = QQ.ravel()

    # Physical wavevectors: G = 2*pi*(p + q*lambda1) / ell_bar
    G_vals = 2.0 * np.pi * (PP + QQ * lam1) / ell_bar

    # Perpendicular component: G_perp_index = q - p/lambda1
    G_perp_idx = QQ - PP / lam1

    # Intensity = sinc^2(pi * G_perp_idx)
    # (Already has pi in the sinc_unnorm definition above)
    intensity = sinc_unnorm(G_perp_idx) ** 2

    # Remove DC component (G ~ 0) and very weak peaks
    mask = np.abs(G_vals) > 1e-10
    G_vals = G_vals[mask]
    intensity = intensity[mask]

    # Keep only positive G (negative ones give the same I by symmetry)
    mask_pos = G_vals > 0
    G_pos = G_vals[mask_pos]
    I_pos = intensity[mask_pos]

    # Sort by G
    order = np.argsort(G_pos)
    G_pos = G_pos[order]
    I_pos = I_pos[order]

    return G_pos, I_pos, p


def lambda_bar_analytical(n, p_max=300, q_max=300, verbose=True):
    """
    Compute Lambda_bar for the n-th metallic-mean chain analytically via:

        Lambda_bar = (2*rho/pi) * sum_{G > 0} I_G / G^2

    Parameters
    ----------
    n : int
        Metallic-mean index.
    p_max, q_max : int
        Summation cutoff.
    verbose : bool
        Print progress.

    Returns
    -------
    lambda_bar : float
    G_pos : ndarray
    I_pos : ndarray
    params : dict
    """
    G_pos, I_pos, params = bragg_peaks_analytical(n, p_max, q_max)
    rho = float(params['rho'])

    # Core sum: sum I_G / G^2  (over distinct positive G values)
    # Peaks at the SAME G (from different (p,q) with same p+q*lambda1) should
    # be summed before dividing by G^2.  In practice, for irrational lambda1,
    # distinct (p,q) pairs give distinct G values.
    sum_val = np.sum(I_pos / G_pos**2)

    lambda_bar = (2.0 * rho / np.pi) * sum_val

    if verbose:
        print(f"\n  {params['name']} (n={n}) -- Analytical method:")
        print(f"    lambda1 = {params['lambda1']:.10f}")
        print(f"    lambda2 = {params['lambda2']:.10f}")
        print(f"    rho     = {rho:.10f}")
        print(f"    ell_bar = {params['ell_bar']:.10f}")
        print(f"    Number of Bragg peaks summed: {len(G_pos)}")
        print(f"    Sum I_G/G^2 = {sum_val:.10f}")
        print(f"    Lambda_bar  = {lambda_bar:.10f}")

    return lambda_bar, G_pos, I_pos, params



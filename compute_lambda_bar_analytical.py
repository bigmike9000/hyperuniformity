def lambda_bar_monte_carlo(chain_name, N_target=500_000, verbose=True):
    """
    Compute Lambda_bar by Monte Carlo number variance computation.
    This is the existing method from quasicrystal_variance.py.
    """
    chain = CHAINS[chain_name]
    M = chain['matrix']

    for iters in range(5, 50):
        vec = np.array([0, 1], dtype=np.int64)
        Mn = np.linalg.matrix_power(M, iters)
        n_pred = int(np.sum(Mn @ vec))
        if n_pred > N_target:
            break

    if verbose:
        print(f"\n  {chain['name']} -- Monte Carlo: generating {iters} iterations...")

    seq = generate_substitution_sequence(chain_name, iters)
    points, L = sequence_to_points(seq, chain_name)
    N = len(points)
    rho = N / L
    mean_spacing = 1.0 / rho

    if verbose:
        print(f"    N = {N:,}, L = {L:.1f}, rho = {rho:.6f}")

    # Variance computation
    R_max = min(500 * mean_spacing, L / 4)
    R_array = np.linspace(mean_spacing, R_max, 2000)

    rng = np.random.default_rng(seed=42)
    variances, _ = compute_number_variance_1d(
        points, L, R_array, num_windows=20000, rng=rng)

    lambda_bar = compute_lambda_bar(R_array, variances)

    if verbose:
        print(f"    Lambda_bar = {lambda_bar:.8f}")

    return lambda_bar, R_array, variances



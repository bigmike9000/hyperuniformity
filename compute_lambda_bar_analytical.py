def compute_structure_factor_fft(chain_name, num_iters=None, N_target=200_000):
    """
    Compute the structure factor S(k) of a substitution tiling via FFT.

    Points are generated by substitution, then S(k) = |rho_hat(k)|^2 / N
    is computed on a dense k-grid.  Bragg peaks appear as tall spikes.

    Returns
    -------
    k : ndarray
        Wavevectors (positive half).
    Sk : ndarray
        Structure factor values.
    points : ndarray
        Point positions.
    L : float
        Domain length.
    """
    chain = CHAINS[chain_name]
    M = chain['matrix']

    if num_iters is None:
        for iters in range(5, 50):
            vec = np.array([0, 1], dtype=np.int64)
            Mn = np.linalg.matrix_power(M, iters)
            n_pred = int(np.sum(Mn @ vec))
            if n_pred > N_target:
                break
        num_iters = iters

    seq = generate_substitution_sequence(chain_name, num_iters)
    points, L = sequence_to_points(seq, chain_name)
    N = len(points)

    # Compute S(k) via DFT on k-grid matched to the domain
    # Use N_fft >> N to get sub-peak resolution
    N_fft = 2 ** int(np.ceil(np.log2(10 * N)))

    # rho_hat(k) = sum_j exp(i k x_j)
    # We evaluate at k = 2*pi*m/L for m = 0, 1, ..., N_fft/2
    # Efficient: use NFFT-style approach via np.dot
    # For moderate N, direct DFT is feasible on ~1000 k values

    k_max = 30.0  # max wavevector
    N_k = 5000    # number of k values
    k = np.linspace(0.01, k_max, N_k)

    # Vectorised computation: shape (N_k, N)
    phase = np.outer(k, points)
    rho_hat = np.sum(np.exp(1j * phase), axis=1)
    Sk = np.abs(rho_hat)**2 / N

    return k, Sk, points, L, N


def find_bragg_peaks(k, Sk, min_height=0.5, min_distance=0.01):
    """
    Find Bragg peak positions and heights from a computed S(k).

    A peak is defined as a local maximum with height > min_height.
    Returns peak k-values and heights.
    """
    from scipy.signal import find_peaks
    peaks, props = find_peaks(Sk, height=min_height, distance=int(min_distance / (k[1]-k[0])))
    return k[peaks], Sk[peaks]


def lambda_bar_fft_method(chain_name, N_target=300_000, k_max=50.0, N_k=8000,
                          verbose=True):
    """
    Compute Lambda_bar via the FFT-based Bragg peak summation.

    Lambda_bar = (2*rho/pi) * sum_{G>0} I_G / G^2

    where I_G are peak heights of S(k)/N extracted from the FFT.

    Parameters
    ----------
    chain_name : str
        Chain key in CHAINS dict.
    N_target : int
        Target number of tiles.
    k_max : float
        Maximum wavevector for the S(k) computation.
    N_k : int
        Number of k-values.

    Returns
    -------
    lambda_bar : float
    G_peaks : ndarray
    I_peaks : ndarray
    rho : float
    """
    chain = CHAINS[chain_name]
    M = chain['matrix']

    # Generate tiling
    for iters in range(5, 50):
        vec = np.array([0, 1], dtype=np.int64)
        Mn = np.linalg.matrix_power(M, iters)
        n_pred = int(np.sum(Mn @ vec))
        if n_pred > N_target:
            break

    if verbose:
        print(f"\n  {chain['name']} -- FFT method: generating {iters} iterations...")

    seq = generate_substitution_sequence(chain_name, iters)
    points, L = sequence_to_points(seq, chain_name)
    N = len(points)
    rho = N / L

    if verbose:
        print(f"    N = {N:,}, L = {L:.1f}, rho = {rho:.6f}")

    # Dense k-grid for S(k) computation
    # We compute S(k) by evaluating the DFT at each k
    k = np.linspace(2.0 * np.pi / L, k_max, N_k)
    dk = k[1] - k[0]

    # Batched DFT computation to save memory
    batch = 500
    Sk = np.zeros(N_k)
    for i in range(0, N_k, batch):
        kb = k[i:i+batch]
        phase = np.outer(kb, points)  # (batch, N)
        rho_hat = np.sum(np.exp(1j * phase), axis=1)
        Sk[i:i+batch] = np.abs(rho_hat)**2 / N

    # Find peaks: local maxima with height > threshold
    from scipy.signal import find_peaks
    min_peak_height = 1.0   # peaks should be >> 1 for Bragg peaks (scale ~ N)
    min_dist_idx = max(1, int(0.02 / dk))   # at least 0.02 apart in k
    peak_idx, props = find_peaks(Sk, height=min_peak_height,
                                 distance=min_dist_idx)

    G_peaks = k[peak_idx]
    I_peaks = Sk[peak_idx] / N   # Normalise to get specific intensity per point

    # Filter: keep only peaks with I > threshold relative to strongest
    if len(I_peaks) > 0:
        I_threshold = 1e-8 * np.max(I_peaks)
        mask = I_peaks > I_threshold
        G_peaks = G_peaks[mask]
        I_peaks = I_peaks[mask]

    # Lambda_bar = (2*rho/pi) * sum I_G / G^2
    if len(G_peaks) > 0:
        sum_val = np.sum(I_peaks / G_peaks**2)
        lambda_bar = (2.0 * rho / np.pi) * sum_val
    else:
        sum_val = 0.0
        lambda_bar = np.nan

    if verbose:
        print(f"    Bragg peaks found: {len(G_peaks)}")
        if len(G_peaks) > 0:
            print(f"    Strongest peak at G = {G_peaks[np.argmax(I_peaks)]:.4f}, "
                  f"I = {np.max(I_peaks):.6f}")
        print(f"    Sum I_G/G^2 (normalised) = {sum_val:.8f}")
        print(f"    Lambda_bar = {lambda_bar:.8f}")

    return lambda_bar, G_peaks, I_peaks, rho, k, Sk



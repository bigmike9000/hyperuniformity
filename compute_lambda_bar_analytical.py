"""
Analytical computation of Lambda_bar for metallic-mean chains using the
Zachary & Torquato (2009) Bragg-peak summation formula:

    Lambda_bar = (2*rho/pi) * sum_{G > 0} I_G / G^2

This is derived from the exact variance formula (their eq. 15 in d=1):
    sigma^2(R) = (rho/pi) * integral S(k) * (2 sin(kR)/k)^2 dk

For a pure Bragg spectrum S(k) = sum_G I_G * delta(k - G), the running average
    Lambda_bar = lim_{R->inf} (1/R) * integral_0^R sigma^2(R') dR'
gives Lambda_bar = (2*rho/pi) * sum_{G>0} I_G / G^2.

This is equivalent to Zachary & Torquato's theta-series method (their eq. 73):
    B_N = lim_{beta->0+} [phi/(2*beta) - (1/2)*sum_k Z_k*r_k*exp(-beta*r_k^2)]
rescaled as Lambda_bar = 2^d * phi * B_N = 2 * phi * B_N at d=1.

Chains treated:
  - Fibonacci  (golden ratio tau = (1+sqrt(5))/2)
  - Silver     (silver ratio sigma = 1+sqrt(2))
  - Bronze     (bronze ratio beta  = (3+sqrt(13))/2)
  - Copper     (copper ratio kappa = 2+sqrt(5))
  - Nickel     (nickel ratio nu    = (5+sqrt(29))/2)

Cross-check: Fibonacci should reproduce Lambda_bar = 0.20110
             (Zachary & Torquato 2009, Table 1).

The script has three methods:
  Method A: Analytical Bragg peak summation using known structure factor
            formula for metallic-mean chains (cut-and-project / eigenfunction
            approach).  Does not require generating the tiling.
  Method B: Large-N FFT of the generated tiling to extract Bragg peak
            positions and intensities numerically, then sum I_G/G^2.
  Method C: Direct variance Monte Carlo (existing approach for comparison).

References:
  Zachary & Torquato (2009) J. Stat. Mech. P12015
  Oguz et al. (2019) -- eigenvalue formula for alpha
"""


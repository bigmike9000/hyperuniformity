def _analytical_silver_closed_form():
    """
    Attempt a closed-form derivation of Lambda_bar for Silver.

    For the n-th metallic-mean chain:
        G_{p,q} = 2*pi*(p + q*lambda1) / ell_bar
        I_{p,q} = sinc^2(pi*(q - p/lambda1))

    Lambda_bar = (2*rho/pi) * sum_{(p,q)!=(0,0)} I_{p,q} / G_{p,q}^2

    Substituting:
        G_{p,q}^2 = (2*pi)^2 * (p + q*lambda1)^2 / ell_bar^2
        I_{p,q} = sin^2(pi*(q - p/lambda1)) / (pi*(q - p/lambda1))^2
                = sin^2(pi*s_{p,q}) / (pi*s_{p,q})^2
        where s_{p,q} = q - p/lambda1

    Lambda_bar = (2*rho/pi) * (ell_bar^2/(4*pi^2)) *
                 sum sin^2(pi*s) / (pi^2*s^2 * (p + q*lam1)^2) * ???

    Let t_{p,q} = p + q*lambda1  (the "parallel" index)
    Let s_{p,q} = q - p/lambda1  (the "perpendicular" index)

    Note: s = q - p/lambda1 = q + p*lambda2  (since lambda2 = -1/lambda1)
    And:  t = p + q*lambda1

    The transformation (p,q) -> (t,s) has Jacobian 1/(lambda1 + 1/lambda1).

    Lambda_bar = (2*rho/pi) * (ell_bar/(2*pi))^2 *
                 sum_{(p,q)!=(0,0)} sinc^2(pi*s_{p,q}) / t_{p,q}^2

    For the INTEGER lattice (lambda1 = 1): all peaks at G = 2*pi*m, I = 1 (delta fns)
    Lambda_bar_Z = (2*rho/pi) * sum_{m=1}^inf 2/(G_m)^2
                 = (2*rho/pi) * 2 * sum_{m=1}^inf ell_bar^2/(4*pi^2*m^2)
                 = (rho*ell_bar^2/pi^3) * pi^2/6 = rho*ell_bar^2/(6*pi)
    With rho*ell_bar = 1: Lambda_bar_Z = ell_bar/(6*pi) * ???
    Hmm, for integer lattice ell_bar=1, rho=1:
    Lambda_bar_Z = (2*1/pi) * 2*sum_{m=1}^inf 1/(2*pi*m)^2 / 1
                 = (2/pi) * (2/(4*pi^2)) * pi^2/6 = (2/pi)*(1/12) = 1/6 ✓
    """
    print("  Checking closed-form structure for Lambda_bar:")
    print()
    print("  Key formula:")
    print("    Lambda_bar = (2*rho/pi) * sum_{G>0} I_G / G^2")
    print()
    print("  With G_{p,q} = 2*pi*(p + q*lam1)/ell_bar")
    print("  and  I_{p,q} = sinc^2(pi*(q - p/lam1))")
    print()

    for n in [1, 2]:
        p = chain_params(n)
        lam1 = float(p['lambda1'])
        rho = float(p['rho'])
        ell_bar = float(p['ell_bar'])
        print(f"  {p['name']}  (n={n}):")
        print(f"    lambda1 = {lam1:.10f}")
        print(f"    rho     = {rho:.10f}")
        print(f"    ell_bar = {ell_bar:.10f}")
        print()

        # Numerical check of series structure
        # Compute partial sums grouped by |t| = |p + q*lam1|
        # to check if sum converges to a simple fraction

        # High-precision summation
        total = 0.0
        count = 0
        P = 3000
        for pp in range(-P, P+1):
            for qq in range(-P, P+1):
                if pp == 0 and qq == 0:
                    continue
                t = pp + qq * lam1
                if t <= 0:
                    continue   # Only positive G
                s = qq - pp / lam1
                if abs(s) < 1e-14:
                    inc = 1.0  # sinc^2(0) = 1
                else:
                    inc = (np.sin(np.pi * s) / (np.pi * s))**2
                G = 2.0 * np.pi * t / ell_bar
                total += inc / G**2
                count += 1

        lb = (2.0 * rho / np.pi) * total
        print(f"    Lambda_bar (P={P}): {lb:.10f}")
        print(f"    Peaks summed: {count}")

        # Check simple fractions
        fracs = [(1,4,'1/4'), (1,5,'1/5'), (1,6,'1/6'), (1,3,'1/3'),
                 (2,9,'2/9'), (3,14,'3/14')]
        print(f"    Comparison to simple fractions:")
        for num, den, label in fracs:
            val = num/den
            print(f"      {label} = {val:.10f}  (diff = {lb-val:+.2e})")
        print()


